package com.onsale.app.board.vo;

public class OwnerBoardVO {
	private int owner_board_num;
	private String mart_name;
	private String owner_board_title;
	private String owner_board_content;
	private String owner_board_date;
	private int owner_board_views;
	
	public OwnerBoardVO() {;}

	public int getOwner_board_num() {
		return owner_board_num;
	}

	public void setOwner_board_num(int owner_board_num) {
		this.owner_board_num = owner_board_num;
	}

	public String getMart_name() {
		return mart_name;
	}

	public void setMart_name(String mart_name) {
		this.mart_name = mart_name;
	}

	public String getOwner_board_title() {
		return owner_board_title;
	}

	public void setOwner_board_title(String owner_board_title) {
		this.owner_board_title = owner_board_title;
	}

	public String getOwner_board_content() {
		return owner_board_content;
	}

	public void setOwner_board_content(String owner_board_content) {
		this.owner_board_content = owner_board_content;
	}

	public String getOwner_board_date() {
		return owner_board_date;
	}

	public void setOwner_board_date(String owner_board_date) {
		this.owner_board_date = owner_board_date;
	}

	public int getOwner_board_views() {
		return owner_board_views;
	}

	public void setOwner_board_views(int owner_board_views) {
		this.owner_board_views = owner_board_views;
	}

	
	
	
	
}
