package com.onsale.app.board.vo;

public class OwnerFilesVO {
	private int owner_board_num;
	private String owner_file_name_original;
	private String owner_file_name;
	
	public OwnerFilesVO() {;}

	public int getOwner_board_num() {
		return owner_board_num;
	}

	public void setOwner_board_num(int owner_board_num) {
		this.owner_board_num = owner_board_num;
	}
	
	public String getOwner_file_name_original() {
		return owner_file_name_original;
	}

	public void setOwner_file_name_original(String owner_file_name_original) {
		this.owner_file_name_original = owner_file_name_original;
	}

	public String getOwner_file_name() {
		return owner_file_name;
	}

	public void setOwner_file_name(String owner_file_name) {
		this.owner_file_name = owner_file_name;
	}
	
	
	
}
