package com.onsale.app.shopping.vo;

public class DeliveryVO {
	private int delivery_num;
	private int mart_num;
	private String delivery_area;
	
	public DeliveryVO() {;}

	public int getDelivery_num() {
		return delivery_num;
	}

	public void setDelivery_num(int delivery_num) {
		this.delivery_num = delivery_num;
	}

	public int getMart_num() {
		return mart_num;
	}

	public void setMart_num(int mart_num) {
		this.mart_num = mart_num;
	}

	public String getDelivery_area() {
		return delivery_area;
	}

	public void setDelivery_area(String delivery_area) {
		this.delivery_area = delivery_area;
	}
	
	
	
}
